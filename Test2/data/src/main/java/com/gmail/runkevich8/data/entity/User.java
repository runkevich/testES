package com.gmail.runkevich8.data.entity;



public class User {


    //Serialized
    private String objectId;
    private String userName;
    private int age;
    private String profileUrl;

    public String getObjectId() {
        return objectId;
    }

    public String getUserName() {
        return userName;
    }

    public int getAge() {
        return age;
    }

    public String getProfileUrl() {
        return profileUrl;
    }
}
